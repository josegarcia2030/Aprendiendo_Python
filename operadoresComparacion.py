#Operadores de Comparacion

'''
==  es igual que 
!=  es distinto de
<   es menor que
<=  es menor o igual que
>   es mayor que
>=  es mayor o igual que
'''

es_igual_que = 5 == 6
print(es_igual_que)

es_distinto_que = 5 != 6
print(es_distinto_que)

mayor_que = 5 > 6
print(mayor_que)

menor_que = 5 < 6
print(menor_que)

mayor_o_igual = 5 >= 6
print(mayor_o_igual)

menor_o_igual = 5 <= 6
print(menor_o_igual)


