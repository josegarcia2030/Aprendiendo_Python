#Operadores Matematicos

#Suma y Resta

suma = 12 + 5
resta = 30 - 18

#Multiplicacion - Division

multiplicar = 4 * 2
dividiendo = 10 / 2 #Nos devuelve un dato float(flotante)

#Potenciacion (exponente) (**)
exponente = 3 ** 3

#Division baja (//)
division_baja = 12 // 2 #devuelve entero redondeado hacia baja

#Resto o modulo 
resto = 30 % 2

print(suma)
print(resta)
print(multiplicar)
print(dividiendo)


tipo_datos = type(2.0)
print(tipo_datos)

print(exponente)
print(division_baja)
print(resto)